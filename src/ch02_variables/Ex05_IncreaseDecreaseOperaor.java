package ch02_variables;

public class Ex05_IncreaseDecreaseOperaor {

	public static void main(String[] args) {
		// 증감연산자
		int num1=10, num2=0, num3=0;
		System.out.println("num1: " + num1 + "num2: " + num2 + "num3: "+num3);
		num2 = ++num1;
		System.out.println("num1: " + num1 + "num2: " + num2 + "num3: "+num3);
	}

}
